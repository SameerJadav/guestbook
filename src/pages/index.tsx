import { type NextPage } from "next";
import Head from "next/head";
import { SignInButton, useUser } from "@clerk/nextjs";

import { api } from "~/utils/api";
import PostWizard from "~/components/PostWizard";

const Home: NextPage = () => {
  const user = useUser();

  const { data, isLoading } = api.post.getAll.useQuery();

  if (isLoading) {
    return <div>Loading...</div>;
  }

  if (!data) {
    return <div>Something went wrong!</div>;
  }

  return (
    <>
      <Head>
        <title>Guestbook</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="min-h-screen w-screen max-w-xl p-4">
        <h1 className="mb-4 text-center font-serif text-4xl font-bold text-white md:mb-6 md:text-5xl">
          Guestbook
        </h1>
        <div className="mb-4 border-b border-dashed border-zinc-400 py-4 text-xl text-white">
          {/* User is not signed in */}
          {!user.isSignedIn && <SignInButton mode="modal" />}
          {/* User is signed in */}
          {user.isSignedIn && <PostWizard />}
        </div>
        {data?.map((post) => (
          <div key={post.id} className="mb-1 flex gap-1 text-lg">
            {/* Change this {user.user?.username} */}
            <p className="text-zinc-300">{user.user?.fullName}:</p>
            <p>{post.content}</p>
          </div>
        ))}
      </main>
    </>
  );
};

export default Home;
